import Foundation

import UIKit

private let reuseIdentifier = "Cell"


class FlickrSearchCollectionViewController: UICollectionViewController {

    var photos = [Photo]()
    
    
    func fetchData() {
    
        if let url = URL(string: "https://api.flickr.com/services/rest/?method=flickr.photos.search&api_key=928dfaf04c50d06076b6d4383e0a26ee&text=harrypotterwallpaper&per_page=60&format=json&nojsoncallback=1") {
            let task = URLSession.shared.dataTask(with: url) { (data, response, error) in
                if let data = data, let searchData = try? JSONDecoder().decode(SearchData.self, from: data) {
                    self.photos = searchData.photos.photo
                    DispatchQueue.main.async {
                        self.collectionView.reloadData()
                    }
                }
                
            }
            task.resume()
        }
        
    }
    
    override func viewDidLoad() {
        super.viewDidLoad()
        
      
        let layout = self.collectionView.collectionViewLayout as? UICollectionViewFlowLayout
        let width = (view.bounds.width - 10) / 2
        layout?.itemSize = CGSize(width: width, height: width + 80)
        fetchData()
    }

